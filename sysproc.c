uint64
sys_exit(void)
{
  int n;
  argint(0, &n);
  exit(n);
  return 0;  // not reached
}

uint64
sys_getpid(void)
{
  return myproc()->pid;
}

uint64
sys_fork(void)
{
  return fork();
}

uint64
sys_wait(void)
{
  uint64 p;
  argaddr(0, &p);
  return wait(p);
}

uint64
sys_sbrk(void)
{
  uint64 addr;
  int n;

  argint(0, &n);
  addr = myproc()->sz;
  if(growproc(n) < 0)
    return -1;
  return addr;
}

uint64
sys_sleep(void)
{
  int n;
  uint ticks0;

  argint(0, &n);
  acquire(&tickslock);
  ticks0 = ticks;
  while(ticks - ticks0 < n){
    if(killed(myproc())){
      release(&tickslock);
      return -1;
    }
    sleep(&ticks, &tickslock);
  }
  release(&tickslock);
  return 0;
}

uint64
sys_kill(void)
{
  int pid;

  argint(0, &pid);
  return kill(pid);
}

// return how many clock tick interrupts have occurred
// since start.
uint64
sys_uptime(void)
{
  uint xticks;

  acquire(&tickslock);
  xticks = ticks;
  release(&tickslock);
  return xticks;
}

// Sets the priority for the calling process equal to num.
// Its permissible values are from 1 (highest priority) to 20 
// (lowest priority).  
// The default priority is 10.
// For successful execution the system calls return 0,
// otherwise they return -1.
uint64
sys_setpriority(void)
{
  int num;
  argint(0, &num);
  return set_priority(num);
}

// This system call will copy into struct pstat * information about
// each active process (pid, ppid, name, priority, status, size
// and whatever else may be needed).
uint64
sys_getpinfo(void)
{
  uint64 pstat; // user pointer to struct pstat
  argaddr(0, &pstat);
  return get_pinfo(pstat);
}
